package study06;

public class DoWhileTest1 {

	public static void main(String[] args) {

		//1 ~ 10
		int a = 1;
		do {
			System.out.println(a);
			a++;
		} while( a<=10 );
		
		System.out.println();
		System.out.println();

		//while
		int b = 1;
		while(b<=10) {
			System.out.println(b);
			b++;
		}
		
		
		

	}

}
